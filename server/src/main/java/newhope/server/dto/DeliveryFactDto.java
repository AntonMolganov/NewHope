package newhope.server.dto;

public class DeliveryFactDto {

    public Long id;
    public Long userId;
    public Long shipperId;
    public String shipperName;
    public Long routeId;
    public String routeName;
    public String routeDescription;
    public DeliveryFactServiceDataDto serviceData;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public Long getShipperId() {
        return shipperId;
    }

    public void setShipperId(Long shipperId) {
        this.shipperId = shipperId;
    }

    public String getShipperName() {
        return shipperName;
    }

    public void setShipperName(String shipperName) {
        this.shipperName = shipperName;
    }

    public Long getRouteId() {
        return routeId;
    }

    public void setRouteId(Long routeId) {
        this.routeId = routeId;
    }

    public String getRouteName() {
        return routeName;
    }

    public void setRouteName(String routeName) {
        this.routeName = routeName;
    }

    public String getRouteDescription() {
        return routeDescription;
    }

    public void setRouteDescription(String routeDescription) {
        this.routeDescription = routeDescription;
    }

    public DeliveryFactServiceDataDto getServiceData() {
        return serviceData;
    }

    public void setServiceData(DeliveryFactServiceDataDto serviceData) {
        this.serviceData = serviceData;
    }

}
